#include "libft.h"

int		ft_countchar(char const *s, char c)
{
	int	x;
	int	z;

	x = 0;
	z = 0;
	while (s[z] != '\0')
	{
		if (s[z] == c)
			x++;
		z++;
	}
	return (x);
}
